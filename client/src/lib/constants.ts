export const getImageForEntity = (name: string, type: 'sector' | 'industry' | 'company'): string => {
  const normalizedName = name.toLowerCase().replace(/[&\s]+/g, ' ').trim();
  
  // For sectors, check exact match first, then partial match
  if (type === 'sector') {
    if (SECTOR_IMAGES[name as keyof typeof SECTOR_IMAGES]) {
      return SECTOR_IMAGES[name as keyof typeof SECTOR_IMAGES];
    }
    
    // Check for partial matches in sector names
    for (const [key, image] of Object.entries(SECTOR_IMAGES)) {
      const normalizedKey = key.toLowerCase().replace(/[&\s]+/g, ' ').trim();
      if (normalizedName.includes(normalizedKey) || normalizedKey.includes(normalizedName)) {
        return image;
      }
    }
  }
  
  // For industries and companies, check industry images first with comprehensive matching
  for (const [key, image] of Object.entries(INDUSTRY_IMAGES)) {
    const normalizedKey = key.toLowerCase().replace(/[&\s]+/g, ' ').trim();
    
    // Check for exact matches, partial matches, and keyword matches
    if (normalizedName.includes(normalizedKey) || 
        normalizedKey.includes(normalizedName) ||
        normalizedName.split(' ').some(word => normalizedKey.includes(word) && word.length > 2) ||
        normalizedKey.split(' ').some(word => normalizedName.includes(word) && word.length > 2)) {
      return image;
    }
  }
  
  // Fallback to sector images for industries
  if (type === 'industry') {
    for (const [key, image] of Object.entries(SECTOR_IMAGES)) {
      const normalizedKey = key.toLowerCase().replace(/[&\s]+/g, ' ').trim();
      if (normalizedName.includes(normalizedKey) || normalizedKey.includes(normalizedName)) {
        return image;
      }
    }
  }
  
  // Default high-quality professional images
  const defaultImages = {
    sector: "https://images.unsplash.com/photo-1557804506-669a67965ba0?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&h=900",
    industry: "https://images.unsplash.com/photo-1560472354-b33ff0c44a43?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&h=900", 
    company: "https://images.unsplash.com/photo-1507003211169-0a1dd7228f2d?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&h=900"
  };
  
  return defaultImages[type];
};
