export const getImageForEntity = (name: string, type: 'sector' | 'industry' | 'company'): string => {
  const normalizedName = name.toLowerCase().replace(/[&\s]+/g, ' ').trim();
  
  // For sectors, check exact match first, then partial match
  if (type === 'sector') {
    if (SECTOR_IMAGES[name as keyof typeof SECTOR_IMAGES]) {
      return SECTOR_IMAGES[name as keyof typeof SECTOR_IMAGES];
    }
    
    // Check for partial matches in sector names
    for (const [key, image] of Object.entries(SECTOR_IMAGES)) {
      const normalizedKey = key.toLowerCase().replace(/[&\s]+/g, ' ').trim();
      if (normalizedName.includes(normalizedKey) || normalizedKey.includes(normalizedName)) {
        return image;
      }
    }
  }
  
  // EXACT MATCH FIRST - Check if the industry name exists exactly in INDUSTRY_IMAGES
  if (INDUSTRY_IMAGES[name as keyof typeof INDUSTRY_IMAGES]) {
    return INDUSTRY_IMAGES[name as keyof typeof INDUSTRY_IMAGES];
  }
  
  // Enhanced industry matching with priority for exact and strong partial matches
  for (const [key, image] of Object.entries(INDUSTRY_IMAGES)) {
    const normalizedKey = key.toLowerCase().replace(/[&\s]+/g, ' ').trim();
    
    // Exact match - highest priority
    if (normalizedName === normalizedKey) {
      return image;
    }
    
    // Strong partial matches - key phrases
    if (normalizedName.includes(normalizedKey) || normalizedKey.includes(normalizedName)) {
      return image;
    }
    
    // Keyword matching for compound industry names
    const nameWords = normalizedName.split(' ').filter(word => word.length > 2);
    const keyWords = normalizedKey.split(' ').filter(word => word.length > 2);
    
    // Check if any significant words match
    if (nameWords.some(word => keyWords.includes(word)) || 
        keyWords.some(word => nameWords.includes(word))) {
      return image;
    }
  }
  
  // Fallback to sector images for industries with keyword matching
  if (type === 'industry') {
    for (const [key, image] of Object.entries(SECTOR_IMAGES)) {
      const normalizedKey = key.toLowerCase().replace(/[&\s]+/g, ' ').trim();
      const keyWords = normalizedKey.split(' ').filter(word => word.length > 2);
      const nameWords = normalizedName.split(' ').filter(word => word.length > 2);
      
      if (normalizedName.includes(normalizedKey) || 
          normalizedKey.includes(normalizedName) ||
          nameWords.some(word => keyWords.includes(word))) {
        return image;
      }
    }
  }
  
  // Default high-quality professional images with unique variations
  const defaultImages = {
    sector: "https://images.unsplash.com/photo-1557804506-669a67965ba0?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&h=900",
    industry: "https://images.unsplash.com/photo-1486406146926-c627a92ad1ab?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&h=900", 
    company: "https://images.unsplash.com/photo-1507003211169-0a1dd7228f2d?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&h=900"
  };
  
  return defaultImages[type];
};
